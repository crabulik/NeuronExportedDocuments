        [Authorize(Roles = IdentityConstants.AdminRole)]
        public ActionResult Index(int? page, AccountsFilter filter)
        {
            int pageNumber = (page ?? 1);
            var accountsList = UserManager.Users;

            if (!string.IsNullOrEmpty(filter.AccountEmail))
                accountsList = accountsList.Where(w => w.Email.Contains(filter.AccountEmail));

            accountsList = accountsList.OrderBy(p => p.Email);
            
            var vm = new AccountsViewModel(UserManager)
            {
                List = accountsList.ToPagedList(pageNumber, AccountsPerPageCount),
                CurrentUserId = User.Identity.GetUserId()
            };
            return View(vm);
        }

        [Authorize(Roles = IdentityConstants.AdminRole)]
        [HttpPost]
        public async Task<ActionResult> DeleteRoles(string userId, string backUrl )
        {
            var vm = new ChangeUserRoleViewModel
            {
                BackUrl = backUrl
            };
            var user = await UserManager.FindByIdAsync(userId);
            if (user != null)
            {
                var canRemove = true;
                if (UserManager.IsInRole(userId, IdentityConstants.AdminRole))
                {
                    var role = await RoleManager.FindByNameAsync(IdentityConstants.AdminRole);
                    if (!(role.Users.Count > 1))
                    {
                        vm.ResultMessage = IdentityResources.rs_DeleteRolesCantDeleteLastAdmin;
                        canRemove = false;
                    }
                }
                if (canRemove)
                {
                    var result = await UserManager.RemoveFromRolesAsync(
                        userId,
                        (await UserManager.GetRolesAsync(userId)).ToArray());
                    vm.ResultMessage = IdentityResources.rs_DeleteRolesSuccess;
                }
            }
            else
            {
                vm.ResultMessage = IdentityResources.rs_UserNotFound;
            }
            return PartialView("_RoleChangeMessage", vm);
        }

        [Authorize(Roles = IdentityConstants.AdminRole)]
        [HttpPost]
        public async Task<ActionResult> SetAdminRole(string userId, string backUrl)
        {
            var vm = new ChangeUserRoleViewModel
            {
                BackUrl = backUrl
            };
            var user = await UserManager.FindByIdAsync(userId);
            if (user != null)
            {
                var canSetRole = true;
                if ((await UserManager.GetRolesAsync(userId)).Count > 0)
                {
                    vm.ResultMessage = IdentityResources.rs_CantSetNewRoleForUserWithRoles;
                    canSetRole = false;
                }
                if (canSetRole)
                {
                    var result = await UserManager.AddToRoleAsync(
                        userId,
                       IdentityConstants.AdminRole);
                    if (result.Succeeded)
                        vm.ResultMessage = IdentityResources.rs_SetAdminRoleSucess;
                    else
                        vm.ResultMessage = IdentityResources.rs_SetAdminRoleError;
                }
            }
            else
            {
                vm.ResultMessage = IdentityResources.rs_UserNotFound;
            }
            return PartialView("_RoleChangeMessage", vm);
        }

